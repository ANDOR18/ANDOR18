package app_prog1;

public class VarDecAndAsgn {

	public static void main(String[] args) {
		boolean isTrue;
		
		isTrue = true;
		System.out.println(isTrue);
		
		isTrue = false;
		System.out.println(isTrue);

		int x;
		x = 6;
		
		System.out.println(x);
		
		double y;
		y = 8.9;
		System.out.println(y);
		y = 0.5;
		System.out.println(y);
		String Name;
		Name = "Kevin";
		System.out.println(Name);
	}

}
