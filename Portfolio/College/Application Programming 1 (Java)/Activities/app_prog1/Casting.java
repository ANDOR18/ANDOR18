package app_prog1;

public class Casting {

	public static void main(String[] args) {
		int num1 = 5;
		int num2 = 3;
		double num3 = 2.5;
		
		
		System.out.println(num1+num2+(int)num3);
		System.out.println((num1+num2)/num3);
		System.out.println((num1*num2)+(int)(num2*num3));
	
	}

}
