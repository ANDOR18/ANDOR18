package app_prog1;

public class ActiviyAreaRec {

	public static void main(String[] args) {
		int length = 6;
		int width = 10;
		int area;
		int circ;
		
		circ = 2*(length+width);
		area = length * width;
		
		System.out.println("Area of Rectangle = " + area + " in^2");				
		System.out.println("Circ of Rectangle = " + circ + " in");		

	}

}
